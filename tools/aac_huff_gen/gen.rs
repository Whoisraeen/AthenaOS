#[derive(Clone)]
struct Quad { code: u32, len: u8, w: i32, x: i32, y: i32, z: i32 }
#[derive(Clone)]
struct Pair { code: u32, len: u8, y: i32, z: i32 }
#[derive(Clone)]
struct Sf { code: u32, len: u8 }

// Build a quad book from codes/bits with (m, offset). Magnitudes if offset 0 (unsigned),
// signed centered values if offset>0.
fn build_quad(codes: &[u32], bits: &[u8], m: i32, o: i32) -> Vec<Quad> {
    let mut v = Vec::new();
    for i in 0..codes.len() {
        let ii = i as i32;
        let w = ii / (m * m * m) - o;
        let x = (ii / (m * m)) % m - o;
        let y = (ii / m) % m - o;
        let z = ii % m - o;
        v.push(Quad { code: codes[i], len: bits[i], w, x, y, z });
    }
    v
}

fn build_pair(codes: &[u32], bits: &[u8], m: i32, o: i32) -> Vec<Pair> {
    let mut v = Vec::new();
    for i in 0..codes.len() {
        let ii = i as i32;
        let y = ii / m - o;
        let z = ii % m - o;
        v.push(Pair { code: codes[i], len: bits[i], y, z });
    }
    v
}

fn build_sf(codes: &[u32], bits: &[u8]) -> Vec<Sf> {
    codes.iter().zip(bits).map(|(&c, &l)| Sf { code: c, len: l }).collect()
}

// Verify dimension, code-fits-length, Kraft sum == 1, prefix-free. Returns Err string.
fn verify(name: &str, expect: usize, codes: &[(u32, u8)]) -> Result<(), String> {
    if codes.len() != expect {
        return Err(format!("{}: dim {} != expected {}", name, codes.len(), expect));
    }
    let mut kraft = 0.0f64;
    let mut bins: Vec<String> = Vec::new();
    for (idx, &(c, l)) in codes.iter().enumerate() {
        if l == 0 {
            return Err(format!("{}: zero-length code at index {}", name, idx));
        }
        if l > 24 {
            return Err(format!("{}: length {} > 24 at index {}", name, l, idx));
        }
        if c >= (1u32 << l) {
            return Err(format!("{}: code {:#x} >= 2^{} at index {}", name, c, l, idx));
        }
        kraft += 1.0 / (1u64 << l) as f64;
        bins.push(format!("{:0w$b}", c, w = l as usize));
    }
    // Prefix-free: no codeword is a prefix of another.
    for i in 0..bins.len() {
        for j in 0..bins.len() {
            if i != j && bins[j].starts_with(&bins[i]) {
                return Err(format!(
                    "{}: '{}' (idx {}) is a prefix of '{}' (idx {})",
                    name, bins[i], i, bins[j], j
                ));
            }
        }
    }
    if (kraft - 1.0).abs() > 1e-9 {
        return Err(format!("{}: Kraft sum {} != 1", name, kraft));
    }
    Ok(())
}

fn quad_codes(q: &[Quad]) -> Vec<(u32, u8)> { q.iter().map(|e| (e.code, e.len)).collect() }
fn pair_codes(p: &[Pair]) -> Vec<(u32, u8)> { p.iter().map(|e| (e.code, e.len)).collect() }
fn sf_codes(s: &[Sf]) -> Vec<(u32, u8)> { s.iter().map(|e| (e.code, e.len)).collect() }

fn emit_quad(name: &str, q: &[Quad]) {
    println!("pub static {}: [HcbQuad; {}] = [", name, q.len());
    let mut line = String::from("    ");
    for (k, e) in q.iter().enumerate() {
        line.push_str(&format!("hq!(0b{:b},{},{},{},{},{}), ", e.code, e.len, e.w, e.x, e.y, e.z));
        if (k + 1) % 4 == 0 { println!("{}", line.trim_end()); line = String::from("    "); }
    }
    if !line.trim().is_empty() { println!("{}", line.trim_end()); }
    println!("];");
}

fn emit_pair(name: &str, p: &[Pair]) {
    println!("pub static {}: [HcbPair; {}] = [", name, p.len());
    let mut line = String::from("    ");
    for (k, e) in p.iter().enumerate() {
        line.push_str(&format!("hp!(0b{:b},{},{},{}), ", e.code, e.len, e.y, e.z));
        if (k + 1) % 6 == 0 { println!("{}", line.trim_end()); line = String::from("    "); }
    }
    if !line.trim().is_empty() { println!("{}", line.trim_end()); }
    println!("];");
}

fn emit_sf(name: &str, s: &[Sf]) {
    println!("pub static {}: [HcbSf; {}] = [", name, s.len());
    let mut line = String::from("    ");
    for (k, e) in s.iter().enumerate() {
        line.push_str(&format!("hs!(0b{:b},{}), ", e.code, e.len));
        if (k + 1) % 6 == 0 { println!("{}", line.trim_end()); line = String::from("    "); }
    }
    if !line.trim().is_empty() { println!("{}", line.trim_end()); }
    println!("];");
}

