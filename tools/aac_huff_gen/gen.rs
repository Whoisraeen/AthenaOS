    if bad {
        eprintln!("VERIFICATION FAILED — not emitting");
        std::process::exit(1);
    }
    eprintln!("All 12 AAC tables verified prefix-free.");

    // Emit the aac_tables.rs codebook arrays.
    emit_quad("AAC_HCB_1", &cb1);
    emit_quad("AAC_HCB_2", &cb2);
    emit_quad("AAC_HCB_3", &cb3);
    emit_quad("AAC_HCB_4", &cb4);
    emit_pair("AAC_HCB_5", &cb5);
