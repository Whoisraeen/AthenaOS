//! UEFI GOP-backed debug console.
//!
//! This module provides a simple text-mode console on top of the linear
//! framebuffer. It supports basic ANSI-like wrapping and scrolling so we
//! can see kernel logs on real hardware without a serial cable.

use crate::framebuffer::{self, FbInfo, Rgb};
use core::fmt;
use core::sync::atomic::{AtomicBool, Ordering};
use spin::Mutex;

/// Gates the serial→GOP framebuffer text mirror (see `serial::_print`). ON
/// during boot so progress is visible on a cableless machine; turned OFF once
/// the desktop is up (`shell_runner::activate_desktop`), because the compositor
/// then owns the framebuffer and raw kernel-log glyphs blitting OVER it made the
/// desktop unusable to test on iron (T1745: the post-boot log stream painted
/// across the screen). Logs still flow to COM1 + the bootlog RAM ring
/// (→ BOOTLOG.TXT) + netlog, so on-iron diagnostics are unaffected — only the
/// on-screen scribble stops. The safe-mode diag panel is a separate compositor
/// surface and is unaffected.
///
/// In a `production` build it starts OFF, so a consumer boot shows NO scrolling
/// boot log at all (UEFI splash → desktop, the Windows/macOS feel); the durable
/// log channels still capture everything for diagnosis.
pub static CONSOLE_MIRROR_ENABLED: AtomicBool = AtomicBool::new(cfg!(not(feature = "production")));

/// Enable/disable the on-screen serial mirror. See [`CONSOLE_MIRROR_ENABLED`].
pub fn set_console_mirror(enabled: bool) {
    CONSOLE_MIRROR_ENABLED.store(enabled, Ordering::Relaxed);
}

/// True while the serial mirror should still paint to the framebuffer.
pub fn console_mirror_enabled() -> bool {
    CONSOLE_MIRROR_ENABLED.load(Ordering::Relaxed)
}

