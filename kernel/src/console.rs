pub struct Console {
    cursor_x: usize,
    cursor_y: usize,
    pub(crate) width: usize,
    pub(crate) height: usize,
    fg_color: Rgb,
    bg_color: Rgb,
}

impl Console {
    pub fn new(info: FbInfo) -> Self {
        Self {
            cursor_x: 0,
            cursor_y: 0,
            width: info.width as usize / FONT_WIDTH,
            height: info.height as usize / FONT_HEIGHT,
            fg_color: Rgb::new(255, 255, 255),
            bg_color: framebuffer::RAEENOS_INK,
        }
    }

    pub fn write_char(&mut self, c: char) {
        match c {
            '\n' => self.newline(),
            '\r' => self.cursor_x = 0,
            '\x08' => {
                // Backspace
                if self.cursor_x > 0 {
                    self.cursor_x -= 1;
                    self.draw_char(self.cursor_x, self.cursor_y, ' ');
                }
            }
            _ => {
                if self.cursor_x >= self.width {
                    self.newline();
                }
                self.draw_char(self.cursor_x, self.cursor_y, c);
                self.cursor_x += 1;
            }
        }
    }

    fn newline(&mut self) {
        self.cursor_x = 0;
        if self.cursor_y + 1 < self.height {
            self.cursor_y += 1;
        } else {
            self.scroll();
        }
    }

    fn scroll(&mut self) {
        // Simple scroll: just clear and reset for now (v0.1)
        // A real scroll would memmove the framebuffer.
        framebuffer::clear(self.bg_color);
        self.cursor_y = 0;
    }

    fn draw_char(&self, x: usize, y: usize, c: char) {
        let glyph_idx = (c as usize).min(127);
        let glyph = &FONT[glyph_idx];

        let px = x * FONT_WIDTH;
        let py = y * FONT_HEIGHT;

        // Single locked blit of the whole 8x8 cell (see framebuffer::draw_glyph_8x8).
        // The old path issued 64 fill_rect calls per glyph and made the
        // serial-mirrored console crawl during boot.
        framebuffer::draw_glyph_8x8(px, py, glyph, self.fg_color, self.bg_color);
    }
}

impl fmt::Write for Console {
    fn write_str(&mut self, s: &str) -> fmt::Result {
        for c in s.chars() {
            self.write_char(c);
        }
        Ok(())
    }
}

pub static CONSOLE: Mutex<Option<Console>> = Mutex::new(None);

pub fn init() {
    if let Some(info) = framebuffer::fb_info() {
        let mut guard = CONSOLE.lock();
        *guard = Some(Console::new(info));
    }
}

/// Boot smoketest: prove serial→GOP text mirroring is live (MasterChecklist §1.1).
pub fn run_boot_smoketest() {
    let dims = {
        let guard = CONSOLE.lock();
        guard.as_ref().map(|c| (c.width, c.height))
    };
    if let Some((width, height)) = dims {
        crate::serial_println!(
            "[console] GOP text fallback active: {}x{} cells ({}x{} px, serial mirrored)",
            width,
            height,
            width * FONT_WIDTH,
            height * FONT_HEIGHT,
        );
    } else {
        crate::serial_println!("[console] GOP text fallback inactive (no framebuffer)");
    }
}

#[doc(hidden)]
pub fn _print(args: fmt::Arguments) {
    use core::fmt::Write;
    let mut guard = CONSOLE.lock();
    if let Some(ref mut console) = *guard {
        let _ = console.write_fmt(args);
    }
}

/// Best-effort mirror used by `serial::_print` for the §1.1 GOP text fallback.
///
/// Uses `try_lock` so it can NEVER deadlock: if CONSOLE is held (by another CPU
/// or a re-entrant interrupt on this one) the line is simply not mirrored to the
/// framebuffer — it still went to the serial port. The serial path must call
/// this *after* releasing the SERIAL1 lock and with interrupts disabled, so the
/// SERIAL1→FB lock-order inversion (and FB re-entrancy from IRQ-context logging)
/// cannot occur.
#[doc(hidden)]
pub fn try_print(args: fmt::Arguments) {
    use core::fmt::Write;
    if let Some(mut guard) = CONSOLE.try_lock() {
        if let Some(ref mut console) = *guard {
            let _ = console.write_fmt(args);
        }
    }
}

#[macro_export]
macro_rules! kprint {
    ($($arg:tt)*) => ($crate::console::_print(format_args!($($arg)*)));
}

#[macro_export]
macro_rules! kprintln {
    () => ($crate::kprint!("\n"));
    ($($arg:tt)*) => ($crate::kprint!("{}\n", format_args!($($arg)*)));
}
