// H.264 CAVLC VLC tables + transform/dequant/deblock constant tables.
//
// All numeric contents are ITU-T H.264 (= ISO/IEC 14496-10) public data tables,
// transcribed and cross-checked FFmpeg `libavcodec/h264*` <-> openh264 `g_kp*` (no code
// copied). The CAVLC VLC tables here are re-verified prefix-free by
// `tools/h264_vlc_gen/gen.rs` (the first proof gate) — a transcription typo fails there
// before it can reach the decoder. The transform/deblock `const` tables (D.3/D.4) are
// reproduced inline from the cited clauses. This file is `include!`d by the generator so
// there is exactly one copy of the data.

/// A single VLC table entry: a code of `len` bits whose value tuple is `(total_coeff,
/// trailing_ones)` for coeff_token, or a single small integer for total_zeros/run_before.
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub struct Vlc {
    pub code: u16,
    pub len: u8,
    /// Primary value (total_coeff, or total_zeros, or run_before).
    pub a: u8,
    /// Secondary value (trailing_ones for coeff_token; 0 otherwise).
    pub b: u8,
}

const fn v(code: u16, len: u8, a: u8, b: u8) -> Vlc {
    Vlc { code, len, a, b }
}

