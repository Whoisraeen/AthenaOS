pub fn sfb_for_rate(rate: u32) -> &'static SfBandIndex {
    match rate {
        44100 => &SFB_44100,
        48000 => &SFB_48000,
        32000 => &SFB_32000,
        22050 => &SFB_22050,
        24000 => &SFB_24000,
        16000 => &SFB_16000,
        11025 => &SFB_11025,
        12000 => &SFB_12000,
        8000 => &SFB_8000,
        _ => &SFB_44100,
    }
}

