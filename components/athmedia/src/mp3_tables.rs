//! ISO/IEC 11172-3 Table B.7 MP3 Huffman big_value tables (the verified subset) +
//! the DSP constant tables (scalefactor-band boundaries, requant power exponents).
//!
//! The Huffman tables are the public, standardized code/length set. They were emitted
//! by `tools/mp3_huff_gen/gen.rs`, which VERIFIES every table is prefix-free and
//! dimension-correct from its (hlen, hcod) source matrices before emitting — a
//! transcription typo fails the generator (and the `mp3_huff_tables_prefix_free`
//! host-KAT) rather than silently producing wrong PCM (CLAUDE media bar: never wrong
//! samples). The full conformant set of codebooks is present:
//! {1,2,3,5,6,7,8,9,10,11,12,13,15,16,24} (tables 4 and 14 are ISO "not used").
//! `crate::mp3::huff_table` maps every `table_select` 0..=31 onto one of these (selects
//! 16..=23 reuse T16, 24..=31 reuse T24, each with its own linbits), and returns a
//! clean `Invalid` only for the non-conformant selects 4/14.
//!
//! Linbits per ISO Table B.7: 0 for the small tables; the escape codebooks T16 (1..13)
//! and T24 (4..13) carry the linbits supplied by `huff_table` per select.

use crate::mp3::HuffEntry;

macro_rules! he {
    ($code:expr, $len:expr, $x:expr, $y:expr) => {
        HuffEntry {
            code: $code,
            len: $len,
            x: $x,
            y: $y,
        }
    };
}

