//! AAC-LC constant tables: the 12 Huffman codebooks (11 spectral + scalefactor),
//! the sampling-frequency + channel-config tables, the scalefactor-band boundary
//! tables, and the filterbank tables (sine + KBD windows, IMDCT pre/post twiddles)
//! computed at init with no-libm trig.
//!
//! The Huffman codebook arrays (`AAC_HCB_1..11`, `AAC_HCB_SF`) are the public,
//! standardized AAC code/length set. They were emitted by `tools/aac_huff_gen/gen.rs`,
//! which VERIFIES every book is dimension-correct, code-fits-length, **Kraft sum == 1**,
//! and prefix-free before emitting — a transcription typo fails the generator (and the
//! `aac_hcb_all_prefix_free` host-KAT) rather than silently producing wrong PCM (CLAUDE
//! media bar: never wrong samples). The value columns are the FINAL signed/unsigned
//! coefficients per ISO Table 4.A (FAAD2 "explicit-column" form), so the runtime decoder
//! does no modulo math. Source: FFmpeg `aactab.c`, cross-checked vs FAAD2 `hcb_*.h`.

use alloc::vec;
use alloc::vec::Vec;

// ── Codebook row types ───────────────────────────────────────────────────────

/// One leaf of a 4-tuple (quad) spectral codebook. For SIGNED books (cb1/2) the
/// w/x/y/z values carry their sign directly; for UNSIGNED books (cb3/4) they are
/// magnitudes 0..2 and the decoder reads one sign bit per nonzero.
#[derive(Clone, Copy)]
pub struct HcbQuad {
    pub code: u32,
    pub len: u8,
    pub w: i32,
    pub x: i32,
    pub y: i32,
    pub z: i32,
}

/// One leaf of a 2-tuple (pair) spectral codebook. SIGNED books (cb5/6) carry sign in
/// y/z; UNSIGNED books (cb7..11) give magnitudes + per-nonzero sign bits. For cb11 a
/// value of 16 is the escape marker (see `aac::get_escape`).
#[derive(Clone, Copy)]
pub struct HcbPair {
    pub code: u32,
    pub len: u8,
    pub y: i32,
    pub z: i32,
}

/// One leaf of the scalefactor DPCM codebook. The decoded index is the array position;
/// the scalefactor delta is `index - 60`.
#[derive(Clone, Copy)]
pub struct HcbSf {
    pub code: u32,
    pub len: u8,
}

macro_rules! hq {
    ($code:expr, $len:expr, $w:expr, $x:expr, $y:expr, $z:expr) => {
        HcbQuad {
            code: $code,
            len: $len,
            w: $w,
            x: $x,
            y: $y,
            z: $z,
        }
    };
}
macro_rules! hp {
    ($code:expr, $len:expr, $y:expr, $z:expr) => {
        HcbPair {
            code: $code,
            len: $len,
            y: $y,
            z: $z,
        }
    };
}
macro_rules! hs {
    ($code:expr, $len:expr) => {
        HcbSf {
            code: $code,
            len: $len,
        }
    };
}

