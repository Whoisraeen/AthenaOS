// ── No-libm math for table build (cosine + I0 Bessel) ─────────────────────────

const PI: f64 = core::f64::consts::PI;

/// No-libm sine via range reduction + Taylor series (good to ~1e-12 over the build range).
fn sin_nl(x: f64) -> f64 {
    // Reduce to [-PI, PI].
    let two_pi = 2.0 * PI;
    let mut t = x % two_pi;
    if t > PI {
        t -= two_pi;
    } else if t < -PI {
        t += two_pi;
    }
    // Taylor around 0 (11 terms is overkill-accurate on [-PI,PI]).
    let x2 = t * t;
    let mut term = t;
    let mut sum = t;
    let mut n = 1.0f64;
    for _ in 0..11 {
        term *= -x2 / ((2.0 * n) * (2.0 * n + 1.0));
        sum += term;
        n += 1.0;
    }
    sum
}

#[inline]
fn cos_nl(x: f64) -> f64 {
    sin_nl(x + PI / 2.0)
}

/// No-libm sqrt via Newton iteration.
fn sqrt_nl(x: f64) -> f64 {
    if x <= 0.0 {
        return 0.0;
    }
    let mut y = x;
    // crude seed
    if y > 1.0 {
        y = x / 2.0;
    }
    for _ in 0..40 {
        y = 0.5 * (y + x / y);
    }
    y
}

/// Modified Bessel function I0(x), series Σ ((x/2)^k / k!)^2, ~30 terms (host build).
fn bessel_i0(x: f64) -> f64 {
    let half = x / 2.0;
    let mut sum = 1.0f64;
    let mut term = 1.0f64;
    for k in 1..40 {
        term *= half / (k as f64);
        sum += term * term;
    }
    sum
}

// ── Filterbank tables (built once at init; sine + KBD half-windows) ───────────

/// The runtime filterbank tables: sine and KBD half-windows for long (1024) and short
/// (128), built no-libm at init. The decoder mirrors each half to form the full window.
pub struct AacFilterTables {
    pub sine_long: Vec<f32>,  // [1024]
    pub sine_short: Vec<f32>, // [128]
    pub kbd_long: Vec<f32>,   // [1024]
    pub kbd_short: Vec<f32>,  // [128]
}

impl AacFilterTables {
    /// Build all four half-windows. Sine: `sin(π/N·(n+0.5))`, n in 0..N/2. KBD: the
    /// cumulative-sqrt Kaiser-Bessel-derived window (α=4 long, α=6 short).
    pub fn build() -> Self {
        Self {
            sine_long: build_sine(2048),
            sine_short: build_sine(256),
            kbd_long: build_kbd(2048, 4.0),
            kbd_short: build_kbd(256, 6.0),
        }
    }
}

/// Sine half-window of length N/2 for a length-N window: `sin(π/N·(n+0.5))`.
pub fn build_sine(n: usize) -> Vec<f32> {
    let half = n / 2;
    let mut w = vec![0.0f32; half];
    for k in 0..half {
        w[k] = sin_nl(PI / (n as f64) * (k as f64 + 0.5)) as f32;
    }
    w
}

/// KBD half-window of length N/2 for a length-N window with parameter α.
/// Kaiser window W[k] for k in 0..=N/2, then `w[n] = sqrt(Σ_{0..=n} W / Σ_{0..=N/2} W)`.
pub fn build_kbd(n: usize, alpha: f64) -> Vec<f32> {
    let half = n / 2;
    let beta = PI * alpha;
    let i0_beta = bessel_i0(beta);
    // Kaiser window samples W[0..=half].
    let mut wk = vec![0.0f64; half + 1];
    for k in 0..=half {
        let r = (k as f64 - (half as f64) / 2.0) / ((half as f64) / 2.0);
        let arg = 1.0 - r * r;
        let arg = if arg < 0.0 { 0.0 } else { arg };
        wk[k] = bessel_i0(beta * sqrt_nl(arg)) / i0_beta;
    }
    let denom: f64 = wk.iter().sum();
    let mut out = vec![0.0f32; half];
    let mut acc = 0.0f64;
    for n_ in 0..half {
        acc += wk[n_];
        out[n_] = sqrt_nl(acc / denom) as f32;
    }
    out
}

/// IMDCT realization for AAC: direct (2/N)·Σ spec[k]·cos((2π/N)(n+n0)(k+1/2)) over the
/// full N outputs. n0 = N/4 + 1/2 per the AAC IMDCT definition. The cosine is computed
/// no-libm. N is 2048 (long) or 256 (short); the spectrum has N/2 coefficients. This is
/// the reference/runtime realization; the host-KAT `aac_imdct_1024_matches_naive`
/// validates it against an independent naive cosine reference.
pub fn imdct(spec: &[f32], out: &mut [f32]) {
    let n = out.len();
    let half = n / 2;
    debug_assert!(spec.len() >= half);
    let n0 = (half as f64 + 1.0) / 2.0; // (N/2 + 1)/2 per ISO §4.6.9.2
    let two_pi_over_n = 2.0 * PI / (n as f64);
    let scale = 2.0 / (n as f64);
    for nn in 0..n {
        let mut acc = 0.0f64;
        let base = two_pi_over_n * (nn as f64 + n0);
        for k in 0..half {
            acc += spec[k] as f64 * cos_nl(base * (k as f64 + 0.5));
        }
        out[nn] = (scale * acc) as f32;
    }
}

// ── TNS reflection-coefficient dequant tables (ISO §4.6.8.3) ──────────────────
//
// Built no-libm at init: 4 small arrays indexed by coded coef value, for the two
// coef_res widths (3-bit and 4-bit) × the two coef_compress settings. The dequant maps
// a coded coef to a PARCOR reflection coefficient via `sin(coef * iqfac)`.

/// Build the TNS dequant table for a given (coef_res, coef_compress).
/// `coef_res` is 3 or 4 (bits); `compress` reduces the resolution by 1.
pub fn tns_dequant_table(coef_res: u8, compress: bool) -> Vec<f32> {
    // Effective resolution bits used for the sine argument.
    let res = coef_res as i32 - if compress { 1 } else { 0 };
    let sign_mask = 1i32 << (res - 1);
    let neg_mask = !((1i32 << res) - 1);
    let count = 1usize << coef_res;
    // iqfac per ISO: PI / (2^res - 0.5) for the upper half, PI / (2^res + 0.5) lower.
    let iqfac = PI / ((1 << (res - 1)) as f64 - 0.5);
    let iqfac_m = PI / ((1 << (res - 1)) as f64 + 0.5);
    let mut t = vec![0.0f32; count];
    for c in 0..count {
        // sign-extend c to res bits
        let mut v = c as i32;
        if compress {
            // when compressed, only res bits are meaningful
            v &= (1 << res) - 1;
        }
        let sv = if v & sign_mask != 0 { v | neg_mask } else { v };
        let tmp = if sv >= 0 {
            (sv as f64) * iqfac
        } else {
            (sv as f64) * iqfac_m
        };
        t[c] = sin_nl(tmp) as f32;
    }
    t
}

